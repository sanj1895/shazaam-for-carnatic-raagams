export const toSargam = (note) => SARGAM_MAP[note] || note;

export const getSwaram = (frequency, saFrequency) => {
    if (!frequency || !saFrequency) return null;

    const ratio = frequency / saFrequency;
    const semitones = 12 * (Math.log(ratio) / Math.log(2));
    const roundedSemitones = Math.round(semitones);

    let interval = roundedSemitones % 12;
    if (interval < 0) interval += 12;

    return SWARAMS[interval];
};

